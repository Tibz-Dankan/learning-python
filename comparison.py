# Boolean, Comparison operator

# Either True or False
print(3.0 == 3) # prints True
print(3> 100) # prints False
print("Dankan" == "Tibz") # prints False


# Not Equal operator
print(4 != 4) # prints False
print("Anni" !="Andy")  #prints True

# Greater than or equal (>=) | Less than or equal (<=)
print(8.0 >= 8)  #print True

# Boolean logic 
# Used to make to more complicated conditions for if statements that reply on more than one condition

# Boolean operator (and)
print(1==1 and 2==2)  #prints True
print(1!=1 and 2==2)  #prints false
print(2>1 and 3>6)   #prints false

# Boolean operator (or)
print(1==1 or 2==2)  #prints True
print(1==1 or 2==3)  #prints True
print(2>1 or 3>6)   #prints false

# Boolean operator (Not), not takes on argument abd inverts
print(not 1==1)
print(not 1>7)

